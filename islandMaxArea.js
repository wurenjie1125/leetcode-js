/**
 * @param {number[][]} grid
 * @return {number}
 */
var maxAreaOfIsland = function(grid) {
  var maxLength = 0;
  var visited = [];
  function hasSearched(pi, pj){
    return visited[pi][pj] === 1
  }
  function findNeighbor(pi, pj) {
    var islandLength = 0;
    if(grid[pi][pj] === 1){
      islandLength = 1;
      //判断是否遍历过该节点
      if(hasSearched(pi, pj)){
        return 0;
      }else{
        visited[pi][pj] = 1;
      }
      //如果没有则继续遍历四个方向
      if(pi > 0){
        islandLength += findNeighbor(pi - 1, pj);
      }

      if(pi < grid.length - 1){
        islandLength += findNeighbor(pi + 1, pj);
      }

      if(pj > 0){
        islandLength += findNeighbor(pi, pj - 1);
      }

      if(pj <  grid[pi].length -1){
        islandLength += findNeighbor(pi, pj + 1);
      }

    }
    return islandLength;
  }
  //初始化记录数组
  for(var i = 0; i < grid.length; i++){
    visited[i] = [];
    for(var j = 0; j < grid[i].length; j++){
      visited[i][j] = 0
    }
  }
  for(var i = 0; i < grid.length; i++){
    for(var j = 0; j < grid[i].length; j++){
      //遍历每个节点
      var islandLength = findNeighbor(i, j);
      if(islandLength > maxLength){
        maxLength = islandLength
      }
    }
  }
  return maxLength;
};
